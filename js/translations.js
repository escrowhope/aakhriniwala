/* English digits -> Urdu numerals, used for prices/phone in Urdu mode */
window.AN_toUrduDigits = function (str) {
  const map = { "0":"۰","1":"۱","2":"۲","3":"۳","4":"۴","5":"۵","6":"۶","7":"۷","8":"۸","9":"۹" };
  return String(str).replace(/[0-9]/g, d => map[d]);
};
